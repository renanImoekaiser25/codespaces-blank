# realizar um código que demanda a entrada de um (R) e efetue o calculo do volume de uma esfera
#entrada do valor de R
R = int(input())
# numero PI, qu será usado no calculo
pi = 3.14159
#calculo do volume
VOLUME = (4/3 * pi * ( R**3))
#mostra o resuldado da operção do volume
print( 'VOLUME = %.3f'%VOLUME)